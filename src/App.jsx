function App() {
  const [currentId, setCurrentId] = useState("intro");
  const [scrollPercent, setScrollPercent] = useState(0);

  const [leftTop, setLeftTop] = useState(null);
  const [rightTop, setRightTop] = useState(null);

  // —Å–ª—É—à–∞–µ–º —Å–∫—Ä–æ–ª–ª –æ–∫–Ω–∞, –∞ –Ω–µ –æ–¥–Ω–æ–≥–æ –±–ª–æ–∫–∞
  useEffect(() => {
    // –ø—Ä–∏ –ø–µ—Ä–µ—Ö–æ–¥–µ –Ω–∞ –¥—Ä—É–≥—É—é –≥–ª–∞–≤—É —Å–±—Ä–∞—Å—ã–≤–∞–µ–º –ø–ª–∞—à–∫–∏
    setLeftTop(null);
    setRightTop(null);
  }, [currentId]);
   useEffect(() => {
    const handleScroll = () => {
      const doc = document.documentElement;
      const scrollTop = window.scrollY || doc.scrollTop || 0;
      const maxScroll = doc.scrollHeight - window.innerHeight;

      if (maxScroll <= 0) {
        setScrollPercent(0);
        return;
      }

      const p = (scrollTop / maxScroll) * 100;
      setScrollPercent(p);

      // –∫–æ–Ω—Ñ–∏–≥ –¥–ª—è —Ç–µ–∫—É—â–µ–≥–æ —Ä–∞–∑–¥–µ–ª–∞
      const cfg = interactiveConfig[currentId] || {};
      const leftCfg = cfg.left;
      const rightCfg = cfg.right;

      // –ª–µ–≤–∞—è –ø–ª–∞—à–∫–∞
      if (leftCfg && leftTop === null && p > (leftCfg.trigger ?? 30)) {
        const offset = leftCfg.offset ?? 0.4;
        const top = scrollTop + window.innerHeight * offset;
        setLeftTop(top);
      }

      // –ø—Ä–∞–≤–∞—è –ø–ª–∞—à–∫–∞
      if (rightCfg && rightTop === null && p > (rightCfg.trigger ?? 70)) {
        const offset = rightCfg.offset ?? 0.4;
        const top = scrollTop + window.innerHeight * offset;
        setRightTop(top);
      }
       
    
    };

    handleScroll(); // –ø–æ—Å—á–∏—Ç–∞—Ç—å —Å—Ä–∞–∑—É –ø—Ä–∏ –º–æ–Ω—Ç–∏—Ä–æ–≤–∞–Ω–∏–∏
    window.addEventListener("scroll", handleScroll);
    return () => window.removeEventListener("scroll", handleScroll);
  }, [currentId, leftTop, rightTop]);


  const currentIndex = sections.findIndex((s) => s.id === currentId);
  const currentSection =
    sections.find((s) => s.id === currentId) || sections[0];

  // –æ–±—â–∏–π –ø—Ä–æ–≥—Ä–µ—Å—Å = –Ω–æ–º–µ—Ä —Ä–∞–∑–¥–µ–ª–∞ + –≤–Ω—É—Ç—Ä–∏ –Ω–µ–≥–æ –ø–æ —Å–∫—Ä–æ–ª–ª—É
  const totalProgress =
    ((currentIndex + scrollPercent / 100) / sections.length) * 100;

  const hasPrev = currentIndex > 0;
  const hasNext = currentIndex < sections.length - 1;

  const scrollToTop = () => {
    window.scrollTo({ top: 0, behavior: "smooth" });
  };

  const goPrev = () => {
    if (!hasPrev) return;
    const prev = sections[currentIndex - 1];
    setCurrentId(prev.id);
    setScrollPercent(0);
    scrollToTop();
  };

  const goNext = () => {
    if (!hasNext) return;
    const next = sections[currentIndex + 1];
    setCurrentId(next.id);
    setScrollPercent(0);
    scrollToTop();
  };

   const cfg = interactiveConfig[currentId] || {};
  const showLeft = leftTop !== null;
  const showRight = rightTop !== null;


  return (
    <div className="app">
      <Header
        currentTitle={currentSection.title}
        progress={totalProgress}
        onPrev={goPrev}
        onNext={goNext}
        hasPrev={hasPrev}
        hasNext={hasNext}
      />

      <div className="layout">
        <Sidebar
          sections={sections}
          currentId={currentId}
          onSelect={(id) => {
            setCurrentId(id);
            setScrollPercent(0);
            scrollToTop();
          }}
        />

        <main className="main">
          <article className="content">
            <h2 className="content-title">{currentSection.title}</h2>
            {currentSection.short && (
              <p className="content-short">{currentSection.short}</p>
            )}
            <SectionBody section={currentSection} />
          </article>
        </main>
      </div>

      {/* –ò–Ω—Ç–µ—Ä–∞–∫—Ç–∏–≤–Ω—ã–µ –ø–ª–∞—à–∫–∏ —Å–ª–µ–≤–∞/—Å–ø—Ä–∞–≤–∞ */}
      <InteractivePanel
        side="left"
        block={cfg.left}
        visible={showLeft}
        top={leftTop}
      />
      <InteractivePanel
        side="right"
        block={cfg.right}
        visible={showRight}
        top={rightTop}
      />
    </div>
  );
}

export default App;
