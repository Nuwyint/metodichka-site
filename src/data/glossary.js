/**
 * Получить определение термина
 */
export function getTermDefinition(term) {
  return glossary.find(
    (item) => item.term.toLowerCase() === term.toLowerCase()
  )?.definition;
}

/**
 * Проверить, является ли слово термином
 */
export function isTerm(word) {
  return glossary.some(
    (item) => item.term.toLowerCase() === word.toLowerCase()
  );
}

